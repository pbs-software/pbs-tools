-- Summarise samples by Trip
SELECT
  OL.GFB_TRIP_ID AS TID_GFB,
  OL.FOS_TRIP_ID AS TID_FOS,
  OL.HAIL_IN_NUMBER AS HAIL,
  OL.OFFLOAD_DATE AS OFF_DATE,
  OL.OFFLOAD_PORT_CODE AS PORT,
  OL.VESSEL_ID AS VID,
  OL.SPECIES_CODE AS SPP_SAMP,
  S.SAMPLE_ID AS SID,
  COUNT(DISTINCT SP.SPECIMEN_ID) AS NFISH,
  SUM(CASE WHEN SP.SPECIMEN_SEX_CODE IN (2) THEN 1 ELSE 0 END) AS NFEM,
  SUM(CASE WHEN SP.SPECIMEN_SEX_CODE IN (1) THEN 1 ELSE 0 END) AS NMAL,
  SUM(CASE WHEN ISNULL(SP.MATURITY_CODE,0) <> 0 THEN 1 ELSE 0 END) AS NMAT,
  SUM(CASE WHEN ISNULL(SP.SPECIMEN_AGE,0) <> 0 THEN 1 ELSE 0 END) AS NAGE,
--  SUM(CASE WHEN ISNULL(SP.SPECIMEN_AGE,0) <> 0 THEN SP.SPECIMEN_AGE ELSE 0 END) AS SAGE,
  SUM(CASE WHEN ISNULL(SP.SPECIMEN_AGE,0) <> 0 THEN SP.SPECIMEN_AGE ELSE 0 END) /   -- AVG with NULLIF does not work for temporary tables
  SUM(CASE WHEN ISNULL(SP.SPECIMEN_AGE,0) <> 0 THEN 1 ELSE 1e-6 END) AS MAGE        -- Cannot divide by 0 if all records are zero
INTO #TRIP_SAMPLES
FROM #OFFLOADS OL
INNER JOIN GFBioSQL.dbo.FISHING_EVENT FE ON
  OL.GFB_TRIP_ID = FE.TRIP_ID
INNER JOIN GFBioSQL.dbo.FISHING_EVENT_CATCH FEC ON
  FE.FISHING_EVENT_ID = FEC.FISHING_EVENT_ID
INNER JOIN GFBioSQL.dbo.CATCH C ON
  FEC.CATCH_ID = C.CATCH_ID
INNER JOIN GFBioSQL.dbo.CATCH_SAMPLE CS ON
  C.CATCH_ID = CS.CATCH_ID
  AND OL.SPECIES_CODE = C.SPECIES_CODE  -- needed to focus on species that were actually sampled, i.e., a small subset of the offloads (like 4 or 5 species) should only be represented in GFBio.
INNER JOIN GFBioSQL.dbo.SAMPLE S ON
  CS.SAMPLE_ID = S.SAMPLE_ID
INNER JOIN GFBioSQL.dbo.SPECIMEN SP ON
  S.SAMPLE_ID = SP.SAMPLE_ID
--WHERE S.SAMPLE_ID IN (584197) -- SGR in 2024
GROUP BY
  OL.GFB_TRIP_ID,
  OL.FOS_TRIP_ID,
  OL.HAIL_IN_NUMBER,
  OL.OFFLOAD_DATE,
  OL.OFFLOAD_PORT_CODE,
  OL.VESSEL_ID,
  OL.SPECIES_CODE,
  S.SAMPLE_ID
ORDER BY
  OL.GFB_TRIP_ID,
  OL.FOS_TRIP_ID,
  OL.HAIL_IN_NUMBER,
  OL.OFFLOAD_DATE,
  OL.OFFLOAD_PORT_CODE,
  OL.VESSEL_ID,
  OL.SPECIES_CODE,
  S.SAMPLE_ID

-- Merge #EMODES (Event modes) and Trip Samples
SELECT
  CONVERT(INTEGER, TS.TID_GFB) AS TID_GFB,
  TS.TID_FOS, TS.HAIL, 
  CONVERT(VARCHAR, TS.OFF_DATE, 20) AS OFF_DATE,
  YEAR(TS.OFF_DATE) AS YEAR,
  MONTH(TS.OFF_DATE) AS MONTH,
  CASE WHEN (MONTH(TS.OFF_DATE) = 2 AND DAY(TS.OFF_DATE) >=21) OR MONTH(TS.OFF_DATE) >= 3 THEN YEAR(TS.OFF_DATE)
    ELSE YEAR(TS.OFF_DATE) - 1 END AS FYEAR,
  TS.PORT, 
  CONVERT(INTEGER, TS.VID) AS VID,
  E.GEAR_MODE, 
  CONVERT(REAL, E.DEPTH_MODE) AS DEPTH_MODE, 
  E.MAJOR_MODE, E.MINOR_MODE, E.LOCAL_MODE, E.MML_MODE, E.SPP_MODE,
  TS.SPP_SAMP, 
  CONVERT(INTEGER, TS.SID) AS SID,
  TS.NFISH, TS.NFEM, TS.NMAL, TS.NMAT, TS.NAGE, 
  CONVERT(REAL, TS.MAGE) AS MAGE
INTO #WHOLE_SHEBANG
FROM
  #EMODES E
  INNER JOIN #TRIP_SAMPLES TS ON
    E.GFB_TRIP_ID = TS.TID_GFB
ORDER BY
  TS.TID_GFB, TS.SID


