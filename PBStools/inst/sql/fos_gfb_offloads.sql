-- Using MERGED_CATCH (because it delineates MW tows)
SELECT
  OT.FOS_TRIP_ID,
  OT.GFB_TRIP_ID,
  ISNULL(MC.GEAR,'UNKNOWN') AS GEAR,
  ISNULL(MC.BEST_DEPTH,0.0) AS DEPTH,
  ISNULL(MC.MAJOR_STAT_AREA_CODE,'00') AS MAJOR,
  ISNULL(MC.MINOR_STAT_AREA_CODE,'00') AS MINOR,
  ISNULL(MC.LOCALITY_CODE,0) AS LOCALITY,
  ISNULL(MC.MAJOR_STAT_AREA_CODE,'00') + '.' + ISNULL(MC.MINOR_STAT_AREA_CODE,'00') + '.' + CONVERT(VARCHAR(5), ISNULL(MC.LOCALITY_CODE,0)) AS MML, -- Major.Minor.Locality
  ISNULL(MC.SPECIES_CODE,'XXX') AS SPECIES
INTO #EVENTS
FROM
  #OFFTRIPS OT
  INNER JOIN GFFOS.dbo.GF_MERGED_CATCH MC ON
    OT.FOS_TRIP_ID = CAST(MC.TRIP_ID AS INT)
WHERE MC.TRIP_ID NOT LIKE '%[a-zA-Z]%'

