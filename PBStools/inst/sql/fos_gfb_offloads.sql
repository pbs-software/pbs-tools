-- Use this to summarise fishing events from GFFOS GF_MERGED_CATCH
-- <https://stackoverflow.com/questions/77694328/get-most-common-value-over-group-by-for-multiple-columns>
-- IF OBJECT_ID('#EMODES') IS NOT NULL DROP TABLE #EMODES -- not necessary
-- WHEN without ELSE returns NULL if condition is not met, BUT WHEN needs ELSE to work in Temporary Tables!!!
SELECT  
  T2.FOS_TRIP_ID, T2.GFB_TRIP_ID,
  MAX(CASE WHEN T2.RN1 = 1 THEN T2.GEAR     ELSE ''   END) AS GEAR_MODE,
  MAX(CASE WHEN T2.RN2 = 1 THEN T2.DEPTH    ELSE 0.0  END) * 1.8288 AS DEPTH_MODE,
  MAX(CASE WHEN T2.RN3 = 1 THEN T2.MAJOR    ELSE '00' END) AS MAJOR_MODE,
  MAX(CASE WHEN T2.RN4 = 1 THEN T2.MINOR    ELSE '00' END) AS MINOR_MODE,
  MAX(CASE WHEN T2.RN5 = 1 THEN T2.LOCALITY ELSE 0       END) AS LOCAL_MODE,
  MAX(CASE WHEN T2.RN6 = 1 THEN T2.MML      ELSE '0.0.0' END) AS MML_MODE,
  MAX(CASE WHEN T2.RN7 = 1 THEN T2.SPECIES  ELSE '000'   END) AS SPP_MODE
INTO #EMODES
FROM (
  SELECT  T1.* ,
    ROW_NUMBER() OVER(PARTITION BY T1.FOS_TRIP_ID, T1.GFB_TRIP_ID ORDER BY T1.GearCnt    DESC, T1.GEAR)     AS RN1,
    ROW_NUMBER() OVER(PARTITION BY T1.FOS_TRIP_ID, T1.GFB_TRIP_ID ORDER BY T1.DepthCnt   DESC, T1.DEPTH)    AS RN2,
    ROW_NUMBER() OVER(PARTITION BY T1.FOS_TRIP_ID, T1.GFB_TRIP_ID ORDER BY T1.MajorCnt   DESC, T1.MAJOR)    AS RN3,
    ROW_NUMBER() OVER(PARTITION BY T1.FOS_TRIP_ID, T1.GFB_TRIP_ID ORDER BY T1.MinorCnt   DESC, T1.MINOR)    AS RN4,
    ROW_NUMBER() OVER(PARTITION BY T1.FOS_TRIP_ID, T1.GFB_TRIP_ID ORDER BY T1.LocalCnt   DESC, T1.LOCALITY) AS RN5,
    ROW_NUMBER() OVER(PARTITION BY T1.FOS_TRIP_ID, T1.GFB_TRIP_ID ORDER BY T1.MMLCnt     DESC, T1.MML)      AS RN6,
    ROW_NUMBER() OVER(PARTITION BY T1.FOS_TRIP_ID, T1.GFB_TRIP_ID ORDER BY T1.SpeciesCnt DESC, T1.SPECIES)  AS RN7
  FROM (
    SELECT *,
      COUNT(E.GEAR)  OVER(PARTITION BY E.FOS_TRIP_ID, E.GFB_TRIP_ID, E.GEAR)  GearCnt,
      COUNT(E.DEPTH) OVER(PARTITION BY E.FOS_TRIP_ID, E.GFB_TRIP_ID, E.DEPTH) DepthCnt,
      COUNT(E.MAJOR) OVER(PARTITION BY E.FOS_TRIP_ID, E.GFB_TRIP_ID, E.MAJOR) MajorCnt,
      COUNT(E.MINOR) OVER(PARTITION BY E.FOS_TRIP_ID, E.GFB_TRIP_ID, E.MINOR) MinorCnt,
      COUNT(E.LOCALITY) OVER(PARTITION BY E.FOS_TRIP_ID, E.GFB_TRIP_ID, E.LOCALITY) LocalCnt,
      COUNT(E.MML)      OVER(PARTITION BY E.FOS_TRIP_ID, E.GFB_TRIP_ID, E.MML)      MMLCnt,
      COUNT(E.SPECIES)  OVER(PARTITION BY E.FOS_TRIP_ID, E.GFB_TRIP_ID, E.SPECIES)  SpeciesCnt
    FROM --(
      #EVENTS E
    --) 
  ) T1 -- Tier 1
) T2 -- Tier 2
GROUP BY T2.FOS_TRIP_ID, T2.GFB_TRIP_ID
ORDER BY T2.FOS_TRIP_ID, T2.GFB_TRIP_ID

