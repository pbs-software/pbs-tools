-- Collect basic offload information (use GFB's FOS_TRIP table rather than link through HAIL_IN, which loses a lot of records)
-- Note: one FOS trip offloads multiple species (no fishing event info because offloads pool fishing events)
-- Had to use aggregates because several instances of multiple offload weights
SELECT --top 1000
  OH.TRIP_ID AS FOS_TRIP_ID,
  OH.OFFLOAD_ID,
  COUNT(OD.OFFLOAD_DETAIL_ID) AS NO_OFFLOAD_DETAILS,  -- some species are recorded more than once in an offload (e.g., 455, 467, 602)
  OH.HAIL_IN_NUMBER,
  OH.OFFLOAD_DATE,
  OH.GEAR,
  OH.OFFLOAD_PORT_CODE,
  OH.OFFLOAD_LOCATION_CODE,
  OH.OFFLOADER_CODE,
  OD.SPECIES_CODE,
  SUM(ISNULL(OD.OFFLOAD_WEIGHT,0)) AS OFFLOAD_WEIGHT,
  OD.WEIGHT_UNIT_CODE,
  T.TRIP_ID AS GFB_TRIP_ID,
  T.TRIP_SUB_TYPE_CODE,
  T.TRIP_START_DATE,
  T.TRIP_END_DATE,
  T.VESSEL_ID,
  T.CAPTAIN_ID
INTO #OFFLOADS
FROM 
  GFFOS.dbo.GF_OFFLOAD_HEADER OH
  INNER JOIN GF_OFFLOAD_DETAIL OD ON
    OH.OFFLOAD_ID = OD.OFFLOAD_ID
  INNER JOIN GFBioSQL.dbo.FOS_TRIP FT ON
    OH.TRIP_ID = FT.FOS_TRIP_ID
  INNER JOIN GFBioSQL.dbo.TRIP T ON
    FT.TRIP_ID = T.TRIP_ID
WHERE 
  T.TRIP_SUB_TYPE_CODE IN (1,4)  -- just get commercial trips
  AND OH.OFFLOAD_PORT_CODE IN (1136, 1123, 1125, 14756) -- Ucluelet, Port Hardy, Prince Rupert, Port Simpson
  AND OH.OFFLOAD_DATE > '2023-03-01'
  --AND OH.GEAR = 'TRAWL' -- gear seems to be 'undefined'
  --AND OH.TRIP_ID IN (435088, 442495, 444213)  -- just for testing
  --AND OH.TRIP_ID IN (447177, 455439, 457021)  -- just for testing
  --AND OH.TRIP_ID IN (438120, 439499, 440776, 441417)  -- just for testing
  --AND OH.TRIP_ID IN (435059, 435088, 435152)  -- just for testing
  --AND OH.TRIP_ID IN (435340)  -- for debugging
  --AND   OD.SPECIES_CODE IN ('222')
GROUP BY
  OH.TRIP_ID,
  OH.OFFLOAD_ID,
  OH.HAIL_IN_NUMBER,
  OH.OFFLOAD_DATE,
  OH.GEAR,
  OH.OFFLOAD_PORT_CODE,
  OH.OFFLOAD_LOCATION_CODE,
  OH.OFFLOADER_CODE,
  OD.SPECIES_CODE,
  OD.WEIGHT_UNIT_CODE,
  T.TRIP_ID,
  T.TRIP_SUB_TYPE_CODE,
  T.TRIP_START_DATE,
  T.TRIP_END_DATE,
  T.VESSEL_ID,
  T.CAPTAIN_ID

